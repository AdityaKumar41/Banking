    // Helper method to validate the account number and check if it exists in the database
private boolean isValidAccountNumber(String accountNumber) {
    try {
        String checkAccountExistenceSQL = "SELECT COUNT(*) FROM Account WHERE AccountNo = ?";
        pst = conn.prepareStatement(checkAccountExistenceSQL);
        pst.setString(1, accountNumber);
        ResultSet resultSet = pst.executeQuery();
        resultSet.next();
        int count = resultSet.getInt(1);

        return count > 0; // If count is greater than 0, account number exists in the database
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e);
        return false; // Assume not valid in case of an exception
    }
}

    
    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        // TODO add your handling code here:
        String tableName = jTextField1.getText();
try {
    String checkTableExistenceSQL = "SHOW TABLES LIKE '" + tableName + "_beneficiary'";
    ResultSet rs = pst.executeQuery(checkTableExistenceSQL);

    if (!rs.next()) {
        // Transaction table does not exist, create it
        String createTransactionTableSQL = "CREATE TABLE " + tableName + "_beneficiary ("
                + "AccountNo INTEGER,"
                + "Name VARCHAR(500),"
                + "Address TEXT,"
                + "AccountT TEXT,"
                + "Email TEXT)";
        pst.executeUpdate(createTransactionTableSQL);
    }

    // Validate the user-provided account number
    String userProvidedAccountNumber = jTextField34.getText();
    if (!isValidAccountNumber(userProvidedAccountNumber)) {
        JOptionPane.showMessageDialog(null, "Invalid account number. Please enter a valid account number.");
        return; // Exit the method if the account number is invalid
    }
if (jTextField34.getText().equals(jTextField1.getText())) {
        JOptionPane.showMessageDialog(null, "You can't add yourself as beneficiary!");
        return;
    }
    
    // Check if the beneficiary already exists in the table
    if (isBeneficiaryDuplicate(tableName + "_beneficiary", userProvidedAccountNumber)) {
        JOptionPane.showMessageDialog(null, "Beneficiary already exists in the table.");
        return;
    }

    // Insert data into the newly created table for a specific account number
    String insertDataSQL = "INSERT INTO " + tableName + "_beneficiary (AccountNo, Name, Address, AccountT, Email) " +
            "SELECT AccountNo, Name, Address, AccountT, Email FROM Account WHERE AccountNo = ?";
    pst = conn.prepareStatement(insertDataSQL);
    pst.setString(1, userProvidedAccountNumber);
    int rowsAffected = pst.executeUpdate();

    if (rowsAffected > 0) {
        // Fetch data from the newly created table and display it in jTable1
        String fetchDataSQL = "SELECT AccountNo, Name, Address, AccountT, Email FROM " + tableName + "_beneficiary";
        pst = conn.prepareStatement(fetchDataSQL);
        rs = pst.executeQuery();
        jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        jTextField34.setText("");
    } else {
        JOptionPane.showMessageDialog(null, "No data found for the provided account number.");
    }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton17ActionPerformed

