private boolean isBeneficiaryDuplicate(String tableName, String accountNumber) {
    try {
        String checkDuplicateSQL = "SELECT COUNT(*) FROM " + tableName + " WHERE AccountNo = ?";
        pst = conn.prepareStatement(checkDuplicateSQL);
        pst.setString(1, accountNumber);
        ResultSet resultSet = pst.executeQuery();
        resultSet.next();
        int count = resultSet.getInt(1);

        return count > 0; // If count is greater than 0, beneficiary is a duplicate
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e);
        return false; // Assume not a duplicate in case of an exception
    }
}

    
